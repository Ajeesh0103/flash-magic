/*****************************************************************************************************
*                                      PORT INTERFACING PROGRAM                                      *
******************************************************************************************************
*                                          HEADER FILES                                              *
*****************************************************************************************************/
#include<lpc21xx.h>
#define DELAY 
#define LCD
#include"Lcd.h"
/*****************************************************************************************************
*                                     FUNCTION INITIALIZATION                                        *
*****************************************************************************************************/
void PORT_Initial(void);	
/*****************************************************************************************************
*                                        CODE AREA                                                   *
*****************************************************************************************************/
int main()
 {
	PORT_Initial();                   /* Function to initialize the direction & pinsel of ports */
	Lcd_init();                       /* Initializing  LCD                                      */
    lCD_string("deviprasad");   /* Initial displays                                       */
	lcd_command1(0xC0);
    lCD_string("     Cochin     ");
	while(1)
	{
		IO0SET=0x000E0000;            /* SET HIGH for LED's                                     */
		delay(15000);	
		IO0CLR=0x000E0000;            /* CLR LED's                                              */
		delay(500);
	}
 }
/*****************************************************************************************************
*                                   PORT INITIALIZATION FUNCTION                                     *
*****************************************************************************************************/
void PORT_Initial(void)	
{
	IO1DIR	=	0x00FE0000;           /* 17-23 Pins As Output for LCD                           */
	IO0DIR	=	0x000E0000;           /* LED(P0.19,P0.18,P0.17)                                 */
	PINSEL0	=	0x00000000;
	PINSEL1	=	0x00000000;
	PINSEL2	=	0x00000000;
} 
